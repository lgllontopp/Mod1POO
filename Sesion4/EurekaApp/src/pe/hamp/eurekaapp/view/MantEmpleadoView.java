private void mostrarEmpleados() {
        DefaultTableModel tabla;
    tabla = (DefaultTableModel) tblDatos1.getModel();
    tabla.setRowCount(0);
   if (lista == null || lista.isEmpty()) {
      return;
    }
    for(EmpleadoBean bean : lista){
        Object[] rowData = {bean.getCodigo(), bean.getPaterno(), bean.getMaterno(), bean.getNombre(), bean.getUsuario()};
      tabla.addRow(rowData);
    }
    }
